#!/bin/bash
# shellcheck disable=SC1071
set -euo pipefail
# ╔═══════════════════════════════════════════════════════════╗
# ║          CubiVeil — Validation Module                     ║
# ║          Валидация пользовательского ввода                ║
# ╚═══════════════════════════════════════════════════════════╝

# ── Валидация домена ─────────────────────────────────────────
# Проверка длины (RFC 1035: max 253 символа)
# Regex проверка + защита от SSRF
# Возвращает 0 если валиден, 1 если невалиден
validate_domain() {
  local domain="$1"

  # Проверка длины (RFC 1035: max 253 символа)
  [[ ${#domain} -gt 253 ]] && return 1

  # Regex проверка: буквы, цифры, дефис, точка; TLD 2+ символа
  [[ ! "$domain" =~ ^[a-zA-Z0-9]([a-zA-Z0-9-]*[a-zA-Z0-9])?(\.[a-zA-Z0-9]([a-zA-Z0-9-]*[a-zA-Z0-9])?)*\.[a-zA-Z]{2,}$ ]] && return 1

  # Защита от SSRF: localhost, .local, IP-адреса
  [[ "$domain" =~ ^localhost$ || "$domain" =~ \.local$ || "$domain" =~ ^[0-9]+\.[0-9]+\.[0-9]+\.[0-9]+$ ]] && return 1

  return 0
}

# ── Валидация email ──────────────────────────────────────────
# Возвращает 0 если валиден, 1 если невалиден
validate_email() {
  local email="$1"
  [[ ! "$email" =~ ^[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\.[A-Za-z]{2,}$ ]] && return 1
  return 0
}

# ── Валидация времени ──────────────────────────────────────────
# Валидация формата времени ЧЧ:ММ
# Часы: 0-23, Минуты: 0-59
# Возвращает 0 если валиден, 1 если невалиден
validate_time() {
  local time="$1"

  # Проверка формата: должно быть ЧЧ:ММ (4 или 5 символов)
  [[ ! "$time" =~ ^[0-9]{1,2}:[0-9]{2}$ ]] && return 1

  # Извлекаем часы и минуты
  local hour
  local minute
  hour=$(echo "$time" | cut -d: -f1)
  minute=$(echo "$time" | cut -d: -f2)

  # Проверка диапазонов
  [[ $hour -lt 0 || $hour -gt 23 ]] && return 1
  [[ $minute -lt 0 || $minute -gt 59 ]] && return 1

  return 0
}

# ── Валидация Chat ID Telegram ───────────────────────────────
# Валидация формата Chat ID
# Для пользователей: положительное число (например: 123456789)
# Для групп/супергрупп: отрицательное число (например: -1001234567890)
# Возвращает 0 если валиден, 1 если невалиден
validate_chat_id() {
  local chat_id="$1"

  # Если пустой — невалидно
  [[ -z "$chat_id" ]] && return 1

  # Проверка формата: только цифры, возможно с минусом для групп
  [[ ! "$chat_id" =~ ^-?[0-9]+$ ]] && return 1

  # Проверка длины: от 1 до 13 цифр (без знака)
  local digits
  digits="${chat_id#-}" # Удаляем минус если есть
  local len=${#digits}
  if [[ $len -lt 1 ]] || [[ $len -gt 13 ]]; then
    return 1
  fi

  return 0
}

# ── Валидация порта ────────────────────────────────────────────
# Валидация номера порта
# Допустимый диапазон: 1-65535
# Системные порты (0-1023) могут требовать root, но разрешены
# Возвращает 0 если валиден, 1 если невалиден
validate_port() {
  local port="$1"

  # Проверка формата: только цифры
  [[ ! "$port" =~ ^[0-9]+$ ]] && return 1

  # Проверка диапазона
  [[ $port -lt 1 || $port -gt 65535 ]] && return 1

  return 0
}

# ── Валидация порта с предупреждением о системных ────────
# Валидация номера порта с рекомендацией
# Возвращает 0 если валиден, 1 если невалиден
# Выводит предупреждение если порт в системном диапазоне (1-1023)
validate_port_safe() {
  local port="$1"

  # Проверка формата и диапазона
  if ! validate_port "$port"; then
    return 1
  fi

  # Предупреждение для системных портов
  if [[ $port -le 1023 ]]; then
    echo "⚠️  Порт $port в системном диапазоне (1-1023), требует root" >&2
  fi

  return 0
}
