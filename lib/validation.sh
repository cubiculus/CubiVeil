# ── Валидация домена ─────────────────────────────────────────
# Проверка длины (RFC 1035: max 253 символа)
# Regex проверка + защита от SSRF
# Возвращает 0 если валиден, 1 если невалиден
validate_domain() {
  local domain="$1"
  
  # Проверка длины (RFC 1035: max 253 символа)
  [[ ${#domain} -gt 253 ]] && return 1
  
  # Regex проверка: буквы, цифры, дефис, точка; TLD 2+ символа
  [[ ! "$domain" =~ ^[a-zA-Z0-9]([a-zA-Z0-9-]*[a-zA-Z0-9])?(\.[a-zA-Z0-9]([a-zA-Z0-9-]*[a-zA-Z0-9])?)*\.[a-zA-Z]{2,}$ ]] && return 1
  
  # Защита от SSRF: localhost, .local, IP-адреса
  [[ "$domain" =~ ^localhost$ || "$domain" =~ \.local$ || "$domain" =~ ^[0-9]+\.[0-9]+\.[0-9]+\.[0-9]+$ ]] && return 1
  
  return 0
}

# ── Валидация email ──────────────────────────────────────────
# Возвращает 0 если валиден, 1 если невалиден
validate_email() {
  local email="$1"
  [[ ! "$email" =~ ^[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\.[A-Za-z]{2,}$ ]] && return 1
  return 0
}

# ── Валидация времени ────────────────────────────────────────
# Формат: HH:MM
# Возвращает 0 если валидно, 1 если невалидно
validate_time() {
  local time="$1"
  local hour min
  
  # Проверка формата
  [[ ! "$time" =~ ^[0-9]{1,2}:[0-9]{2}$ ]] && return 1
  
  hour=$(echo "$time" | cut -d: -f1)
  min=$(echo "$time" | cut -d: -f2)
  
  # Убираем ведущие нули для арифметического сравнения
  hour=$((10#$hour))
  min=$((10#$min))
  
  [[ $hour -lt 0 || $hour -gt 23 ]] && return 1
  [[ $min -lt 0 || $min -gt 59 ]] && return 1
  
  return 0
}

# ── Валидация Chat ID Telegram ───────────────────────────────
# Telegram chat ID: отрицательные для групп, положительные для пользователей
# Возвращает 0 если валиден, 1 если невалиден
validate_chat_id() {
  local chat_id="$1"
  
  # Проверка формата: целое число (может быть отрицательным)
  [[ ! "$chat_id" =~ ^-?[0-9]+$ ]] && return 1
  
  # Проверка диапазона: -1000000000 до 9999999999
  [[ $chat_id -lt -1000000000 || $chat_id -gt 9999999999 ]] && return 1
  
  return 0
}

# ── Валидация порта ──────────────────────────────────────────
# Возвращает 0 если валиден, 1 если невалиден
validate_port() {
  local port="$1"
  
  # Проверка что это число
  [[ ! "$port" =~ ^[0-9]+$ ]] && return 1
  
  # Проверка диапазона 1-65535
  [[ $port -lt 1 || $port -gt 65535 ]] && return 1
  
  return 0
}
