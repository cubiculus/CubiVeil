# ── Цвета / Colors ───────────────────────────────────────────
# shellcheck disable=SC2034
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[0;33m'
BLUE='\033[0;34m'
CYAN='\033[0;36m'
PLAIN='\033[0m'

# ── Функции вывода / Output functions ─────────────────────────

# Информационное сообщение
info() {
  echo -e "ℹ️  $*"
}

# Успешное сообщение
success() {
  echo -e "✅ $*"
}

# Предупреждение
warning() {
  echo -e "⚠️  $*"
}

# Ошибка с выходом
err() {
  echo -e "❌ $*" >&2
  exit 1
}

# Успешная отметка (совместимость с lib/fallback.sh)
ok() {
  echo -e "${GREEN}[✓]${PLAIN} $1"
}

# Предупреждающая отметка (совместимость с lib/fallback.sh)
warn() {
  echo -e "${YELLOW}[!]${PLAIN} $1"
}

# ── Функции шагов / Step functions ───────────────────────────

# Заголовок шага с номером и локализацией
step_title() {
  local step="$1"
  local ru="$2"
  local en="$3"

  echo ""
  echo "══════════════════════════════════════════════════════════"
  if [[ "${LANG_NAME:-}" == "Русский" ]]; then
    echo "  ${step}. ${ru}"
  else
    echo "  ${step}. ${en}"
  fi
  echo "══════════════════════════════════════════════════════════"
}

# Простой заголовок шага (совместимость с lib/fallback.sh)
step() {
  echo -e "\n${BLUE}━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━${PLAIN}"
  echo -e "${BLUE}  $1${PLAIN}"
  echo -e "${BLUE}━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━${PLAIN}"
}
